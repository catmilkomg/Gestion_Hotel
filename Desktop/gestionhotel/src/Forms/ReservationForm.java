    public void print() throws FileNotFoundException, DocumentException, PrinterException {
      
	

		document = new Document();
		String p="";
		JFileChooser jf=new JFileChooser();
		jf.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
		int x=jf.showSaveDialog(this);
		if(x==JFileChooser.APPROVE_OPTION) {
			p=jf.getSelectedFile().getPath();
		}
		PdfWriter.getInstance(document, new FileOutputStream(p+".pdf"));
		document.open();
		PdfPTable ptd=new PdfPTable(5);
		ptd.addCell("ID");
		ptd.addCell("ID CLIENT");
		ptd.addCell("ID CHAMBRE");
		ptd.addCell("DATE DEBUT");
		ptd.addCell("DATE FIN");
		for(int i=0;i<RES.getRowCount();i++) {
			 String idd= RES.getValueAt(i, 0).toString();
			String id_clt=RES.getValueAt(i, 1).toString();
			String id_chm=RES.getValueAt(i, 2).toString();
			String dD=RES.getValueAt(i, 3).toString();
			String dF=RES.getValueAt(i, 4).toString();
			ptd.addCell(idd);
			ptd.addCell(id_clt);
			ptd.addCell(id_chm);
			ptd.addCell(dD);
			ptd.addCell(dF);
			    
		}
		
		document.add(ptd);
		document.close();

}
