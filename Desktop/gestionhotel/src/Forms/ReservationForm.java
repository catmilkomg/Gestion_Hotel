    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
           Client nom = (Client) clientText.getSelectedItem();
        Chambre chambre = (Chambre) chambretxt.getSelectedItem();
           java.sql.Date  dated =  new java.sql.Date(dateD.getDate().getTime());
        java.sql.Date datef = new java.sql.Date(dateF.getDate().getTime());
        Reservation res = cs.findById(id);
        res.setL(nom);
        res.setCh(chambre);
        res.setDatedebut(dated);
        res.setDatefin(datef);
        int reponse = JOptionPane.showConfirmDialog(this, "vous voulez vraiment modifier cette reservation");
        if(reponse==0){
             if (cs.update(res)) {
            JOptionPane.showMessageDialog(this, "reservation bien modifier");}
        load();}
    }//GEN-LAST:event_jButton2ActionPerformed

