    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String code= codetxt.getText();
        String libelle=libelletxt.getText();
        
        if(cs.create(new Categorie(code,libelle)))
            JOptionPane.showMessageDialog(this, "Categorie bien ajout√©");
        load();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String code=codetxt.getText();
        String libelle=libelletxt.getText();

        Categorie categorie=cs.findById(id);
        categorie.setCode(code);
        categorie.setLibelle(libelle);
        int reponse=JOptionPane.showConfirmDialog(this, "voulez-vous vraiment modifier ce Categorie");
        if(reponse==0){
            if(cs.update(categorie))
                JOptionPane.showMessageDialog(this, "Categorie bien modifi√©");
        }
        load();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void ListeClientsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListeClientsMouseClicked
       id=Integer.parseInt(model.getValueAt(ListeClients.getSelectedRow(), 0).toString());
       codetxt.setText(model.getValueAt(ListeClients.getSelectedRow(), 1).toString());
       libelletxt.setText(model.getValueAt(ListeClients.getSelectedRow(), 2).toString());
     
    }//GEN-LAST:event_ListeClientsMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int reponse=JOptionPane.showConfirmDialog(this, "voulez vous vraiment supprimer ce client");
        if(reponse==0){
            if(cs.delete(cs.findById(id)))
                JOptionPane.showMessageDialog(this, "Categorie bien supprim√©");
            load();
        }
    }//GEN-LAST:event_jButton3ActionPerformed


